<p>инструкция приготовления</p>
<p id="time">30 минут</p>
<ol>
    <li>На водяной бане растопить 125 грамм сливочного масла, добавить сахар, мед, размешать и нагревать, периодически помешивая. Нужно чтобы масса стала однородной.</li>
    <li>Добавить разрыхлитель (если под рукой разрыхлителя нет, подойдет сода в том же объеме) и мешать до тех пор, пока масса не побелеет. Снять с бани и остудить.</li>
    <li>Добавить яйца и хорошо размешать. Затем аккуратно ввести муку и замесить мягкое тесто. Разделить на 6-8 частей и раскатать коржи (удобнее всего раскатывать тесто прямо в форме, это можно сделать ложкой, размяв его по дну, так как тесто слишком мягкое и может прилипать к скалке). Выпекать коржи в нагретой до 170-180 градусов духовке примерно 5-10 минут.</li>
    <li>Для крема 300 грамм размягченного сливочного масла взбить миксером до получения пышной массы</li>
    <li>Затем, не прекращая взбивать, добавить в масло небольшими порциями вареное сгущенное молоко и снова взбить все вместе до получения пышной однородной массы.</li>
    <li>Готовые коржи сложить друг на друга и ровно обрезать края с помощью ножа. Промазать коржи кремом и сложить один на другой, последний верхний корж также обмазать кремом, бока торта — тоже. Украсить толчеными грецкими орехами.</li>
</ol>



